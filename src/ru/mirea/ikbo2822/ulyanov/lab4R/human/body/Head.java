package ru.mirea.ikbo2822.ulyanov.lab4R.human.body;

public class Head {
    private int eyes;
    private int nose;
    private int mouth;

    public Head(int eyes, int nose, int mouth) {
        this.eyes = eyes;
        this.nose = nose;
        this.mouth = mouth;
    }

    public int getEyes() {
        return eyes;
    }

    public int getNose() {
        return nose;
    }

    public int getMouth() {
        return mouth;
    }
}
