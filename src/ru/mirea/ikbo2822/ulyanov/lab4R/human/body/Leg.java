package ru.mirea.ikbo2822.ulyanov.lab4R.human.body;
public class Leg {
    private int length;
    private int strength;

    public Leg(int length, int strength) {
        this.length = length;
        this.strength = strength;
    }

    public int getLength() {
        return length;
    }

    public int getStrength() {
        return strength;
    }
}
